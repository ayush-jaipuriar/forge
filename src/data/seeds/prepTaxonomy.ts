import type { PrepTopicSeed } from '@/domain/prep/types'

function topic(id: string, domain: PrepTopicSeed['domain'], group: string, title: string): PrepTopicSeed {
  return {
    id,
    domain,
    group,
    title,
    defaultConfidence: 'low',
    defaultExposureState: 'notStarted',
    readinessLevel: 'building',
  }
}

